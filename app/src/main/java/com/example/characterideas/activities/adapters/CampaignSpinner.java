package com.example.characterideas.activities.adapters;

public class CampaignSpinner {
    private int id;
    private String name;

    public CampaignSpinner(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
